#include "main.h"
#include "i2c-lcd.h"
#include "stm32f1xx_it.h"



/* Private variables ---------------------------------------------------------*/
I2C_HandleTypeDef hi2c1;

TIM_HandleTypeDef htim1;


/* Private function prototypes -----------------------------------------------*/
void SystemClock_Config(void);  
static void MX_GPIO_Init(void);  
static void MX_I2C1_Init(void);	
static void MX_TIM1_Init(void);	 


/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
 
  HAL_Init(); 

  SystemClock_Config(); 


  MX_GPIO_Init(); 	
  MX_I2C1_Init();		 
  MX_TIM1_Init();    
 
	HAL_TIM_IC_Start_IT(&htim1, TIM_CHANNEL_1);
  
	lcd_init();
	lcd_clear();
  
  while (1)
  {
    
		lcd_put_cur(0,0);
		lcd_send_string(" F =");
		lcd_put_cur(0,14);
		lcd_send_string("Hz");
		lcd_put_cur(1,0);
		lcd_send_string(" v =");
		lcd_put_cur(1,12);
		lcd_send_string("km/h");
		TIM1_CC_IRQHandler();
		HAL_Delay(20000);
  }
}

